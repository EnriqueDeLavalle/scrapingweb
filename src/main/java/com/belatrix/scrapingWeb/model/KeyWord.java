package com.belatrix.scrapingWeb.model;

public class KeyWord {
	
	private String word;

	public KeyWord() {
		super();
	}

	public KeyWord(String word) {
		super();
		this.word = word;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}
	

}
